def sx_dt1(addr, data):
    cs = (128 - ((sum(addr) + sum(data)) % 128)) % 128
    msg = [0xF0, 0x41, 0x10, 0x01, 0x05, 0x07, 0x12, *addr, *data, cs, 0xF7]
    return " ".join(f"{b:02X}" for b in msg)


def amidi_send(port, msg):
    subprocess.run(["amidi", "-p", port, "-S", msg], check=True)


def apply_variant(port, i):
    v = VARIANTS[i]

    # editor on + patch 4
    amidi_send(port, "F0 41 10 01 05 07 12 7F 00 00 01 01 7F F7")
    amidi_send(port, "F0 41 10 01 05 07 12 7F 00 01 00 00 04 7C F7")

    # core tone
    amidi_send(port, sx_dt1([0x20, 0x00, 0x06, 0x00], v["amp"]))      # AMP
    amidi_send(port, sx_dt1([0x20, 0x00, 0x0A, 0x00], v["booster"]))  # BOOSTER(1)
    amidi_send(port, sx_dt1([0x20, 0x00, 0x4C, 0x00], [0x01, 0x00, 0x00]))  # EQ on
    amidi_send(port, sx_dt1([0x20, 0x00, 0x54, 0x00], v["ge10"]))     # GE10
    amidi_send(port, sx_dt1([0x20, 0x00, 0x58, 0x00], [0x01, 0x32, 0x28]))  # NS

    # keep default dry on patch recall
    amidi_send(port, sx_dt1([0x20, 0x00, 0x08, 0x00], [0x01, 0x00, 0x00, 0x00, 0x00, 0x00]))
    amidi_send(port, sx_dt1([0x20, 0x00, 0x34, 0x0A], [0x00]))
    amidi_send(port, sx_dt1([0x20, 0x00, 0x36, 0x0A], [0x00]))
    amidi_send(port, sx_dt1([0x20, 0x00, 0x38, 0x0A], [0x00]))
    amidi_send(port, sx_dt1([0x10, 0x00, 0x1A, 0x03], [0x00]))
    amidi_send(port, sx_dt1([0x10, 0x00, 0x1A, 0x04], [0x00]))
    amidi_send(port, sx_dt1([0x10, 0x00, 0x1C, 0x03], [0x00]))
    amidi_send(port, sx_dt1([0x10, 0x00, 0x1C, 0x04], [0x00]))

    return v


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--port", default=PORT_DEFAULT)
    ap.add_argument("--index", type=int, default=None, help="0-based variant index")
    ap.add_argument("--next", action="store_true")
    args = ap.parse_args()

    if not os.path.exists(STATE_PATH):
        with open(STATE_PATH, "w") as f:
            json.dump({"next_index": 0}, f, indent=2)

    with open(STATE_PATH) as f:
        state = json.load(f)

    if args.index is not None:
        idx = args.index % len(VARIANTS)
    elif args.next:
        idx = state.get("next_index", 0) % len(VARIANTS)
    else:
        idx = 0

    v = apply_variant(args.port, idx)
    state["next_index"] = (idx + 1) % len(VARIANTS)
    state["last_applied"] = {
        "index": idx,
        "name": v["name"],
        "at": datetime.now().isoformat(timespec="seconds"),
    }
    with open(STATE_PATH, "w") as f:
        json.dump(state, f, indent=2)

    if not os.path.exists(LOG_PATH):
        with open(LOG_PATH, "w") as f:
            f.write("# Variation Cycle Log\n\n")
    with open(LOG_PATH, "a") as f:
        f.write(f"## {datetime.now().isoformat(timespec='seconds')} - {v['name']} (index {idx})\n")
        f.write(f"- amp: `{v['amp']}`\n")
        f.write(f"- booster: `{v['booster']}`\n")
        f.write(f"- ge10: `{v['ge10']}`\n")

    print(f"Applied {v['name']} (index {idx}). Next index: {state['next_index']}")


if __name__ == "__main__":
    main()
